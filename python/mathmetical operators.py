num = int(input("Enter the firt num: "))
num2 = int(input("Enter the second num: "))

operator = input("Enter the operator: ")

if operator == "+":
    print(num+num2)
elif operator == "-":
    print(num-num2)
elif operator == "/":
    print(num/num2)
elif operator == "%":
    print(num%num2)
elif operator == "*":
    print(num*num2)
